package com.myjavastudy.a_basics.day01_flowcontrol;

//switch分支执行判断语句

public class T_switch01 {
    public static void main(String[] args) {
        int i = 20;
        switch (i){
            case 10 :
                System.out.println("分支一");
                break;
            case 20 :
                System.out.println("分支二");
                break;
            case 30 :
                System.out.println("分支三");
                break;
        }
    }

}
